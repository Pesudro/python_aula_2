while True:
    numero = int(input("Digite um numero entre 1-5:"))
    if numero > 5:
        print("Número inválido")
    elif numero < 1:
        print("Número inválido")
    else:
        break

if numero == 1:
    print("O numero digitado escrito por extenso é: Um")

elif numero == 2:
    print("O numero digitado escrito por extenso é: Dois")

elif numero == 3:
    print("O numero digitado escrito por extenso é: Três")

elif numero == 4:
    print("O numero digitado escrito por extenso é: Quatro")

elif numero == 5:
    print("O numero digitado escrito por extenso é: Cinco")


